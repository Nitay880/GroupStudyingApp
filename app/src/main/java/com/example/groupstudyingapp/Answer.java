package com.example.groupstudyingapp;

public class Answer {

    private String id;
    private String link;
    private float rating;

    Answer(){}

    /////////////////////////////////// Getters ////////////////////////////////////////////////////
    public String getId() {
        return id;
    }

    public String getLink() {
        return link;
    }

    public float getRating() {
        return rating;
    }

    /////////////////////////////////// Setters ////////////////////////////////////////////////////
    public void setId(String id) {
        this.id = id;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public void setRating(float rating) {
        this.rating = rating;
    }






}
